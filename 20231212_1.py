#數字函式使用

#str()
number = 8
print(str(number))

#abs() 絕對值
print(abs(number))

#pow() 次方
print(pow(2,4))

#max() 取最大
print(max(1,2,3,4,5))

#min() 取最小
print(min(1,2,3,4,5))

#round() 取整數
print(round(4.6))

from math import *  #這是一個模組的引數，可使用更多數學函式

#floor() 無條件捨去
print(floor(5.1))

#ceil() 無條件進位
print(ceil(5.1))